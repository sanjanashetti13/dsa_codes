using namespace std;

int main(){
    int n = 3;

    for(int i = 0; i <= n; i++){

      for(int j = 0; j <= n-i-1; j++){

        cout<< "  ";

        }  

      for(int k = 0; k < 2*i+1; k++){

          cout << "* ";
      }


        cout << endl;
        
    }


    for(int i = 0; i <= n; i++){
      for(int j = 1; j <= i+1; j++){
        cout << "  ";
      }

      for(int k = 0; k < 2*(n-i-2); k++){
        cout << "* ";


      }

     cout << endl;
    }



  return 0;

}


